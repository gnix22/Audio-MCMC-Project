## creation of mcmc using Metropolis-Hastings
# here I will be using a Laplace distribution, as that seems to be the general consensus when it comes to sparse data. 
# all credit for most of the code goes to https://github.com/Joseph94m/MCMC/blob/master/MCMC.ipynb
# create the proposal_distribution that determines the parameter step sizes that our parameters will explore within the parameter space.
proposal_distribution = lambda theta: [np.random.normal(theta[0],0.01), # new mu proposal
                                       np.random.normal(theta[1],0.001)] # slightly less for b since b is sensitive in laplacian
# the prior forms prior assumptions that help with the uncertainty that we have about our parameter space.
# the use of non-uniform priors in this case work to act as a regularization factor, as opposed to a lack of it when using uniform priors.
# we use a log prior for numerical stability as we are using these non-uniform priors
def log_prior(theta):
    mu, b = theta[0], theta[1]
    if b <= 0:
        return -np.inf  # hard constraint, log(0) = -inf
    log_prior_mu = scipy.stats.norm(0, 1).logpdf(mu)
    log_prior_b = scipy.stats.halfnorm(0, 1).logpdf(b)
    return log_prior_mu + log_prior_b

# using prior distribution assuming data is generated by a laplacian, the log-likelihood function is given by
def laplace_log_likelihood(theta, data):
    mu, b = theta[0], theta[1]
    if b <= 0:
        return -np.inf
    n = len(data)
    return -n * np.log(2 * b) - (1 / b) * np.sum(np.abs(data - mu))
def log_posterior(theta, data):
    return laplace_log_likelihood(theta, data) + log_prior(theta)
def acceptance(theta, theta_new):
    if theta_new > theta:
        return True
    else:
        accept = np.random.uniform(0, 1)
        return (np.log(accept) < (theta_new - theta))  # staying in log space throughout
def metropolis_hastings(data, initial_params, n_samples):
    theta_current = initial_params
    accepted = []
    rejected = []
    for _ in range(n_samples):
        theta_new = proposal_distribution(theta_current)
        current_log_post = log_posterior(theta_current, data)
        new_log_post     = log_posterior(theta_new,     data)
        if acceptance(current_log_post, new_log_post):
            theta_current = theta_new
            accepted.append(theta_current)
        else:
            rejected.append(theta_new)
    print(f"Acceptance rate: {len(accepted) / n_samples:.2%}\n")
    return np.array(accepted), np.array(rejected)
accepted, rejected = metropolis_hastings(data, [np.mean(data), np.mean(np.abs(data - np.mean(data)))], 10000)
accepted_mu_samples = accepted[:,0]
accepted_b_samples = accepted[:,1]
rejected_mu_samples = rejected[:,0]
rejected_b_samples = rejected[:,1]

